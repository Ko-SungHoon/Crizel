);

CREATE TABLE TWITTER(
NAME	VARCHAR2(100 CHAR),
ADDR	VARCHAR2(100 CHAR)
);

CREATE TABLE DIARY
(
    DIARY_ID         NUMBER    NOT NULL, 
    DIARY_DATE       DATE      NULL, 
    CONTENT          CLOB      NULL, 
    DELETE_STATUS    CHAR      NULL, 
    CONSTRAINT DIARY_PK PRIMARY KEY (DIARY_ID)
);

